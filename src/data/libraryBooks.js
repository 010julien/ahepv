// Filter functions
export const filterBooks = (books, filters) => {
  return books.filter(book => {
    if (filters.level && book.level !== filters.level) return false;
    if (filters.grade && book.grade !== filters.grade) return false;
    if (filters.subject && book.subject !== filters.subject) return false;
    if (filters.search) {
      const searchLower = filters.search.toLowerCase();
      return (
        book.title.toLowerCase().includes(searchLower) ||
        book.author.toLowerCase().includes(searchLower) ||
        book.subject.toLowerCase().includes(searchLower)
      );
    }
    return true;
  });
};

export const getAllBooks = () => {
  return [
    ...libraryBooks.free,
    ...libraryBooks.premium,
    ...libraryBooks.borrow,
  ];
};
